# Use a variable to represent a person’s name, and then print that person’s name in lowercase, uppercase, and title case.
# Kess Ahmed, 15th July 2025

# Declare person name
person_name = "ELsA"

# Print upper
print(f"{person_name.upper()}")

# Print lower
print(f"{person_name.lower()}")

# Print title
print(f"{person_name.title()}")
