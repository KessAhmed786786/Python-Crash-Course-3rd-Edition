# alien_color variable, use a if-else chain

# Create variable
alien_color = 'green'

# if-else block
if alien_color == 'green':
    print("You earned 5 points!")
else:
    print("You earned 10 points!")

# Repeat to see else output
alien_color = 'red'

if alien_color == 'green':
    print("You earned 5 points!")
else:
    print("You earned 10 points!")
