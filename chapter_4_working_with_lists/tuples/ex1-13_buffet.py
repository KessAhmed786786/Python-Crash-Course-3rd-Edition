# Store 5 foods in tuple, Use for loop and print, revise the menu with 2 new elements

# Create tuple called menu, assign 5 immutable dishes
menu = ('jacket potato', 'shrimp', 'sea bass', 'new york steak', 'mushroom salad')

# For loop iterate print
for dish in menu:
    print(dish)

# Reassign menu, change 2 menu items
menu = ('roast chicken', 'falafel', 'sea bass', 'new york steak', 'mushroom salad')

# For loop iterate print
for dish in menu:
    print(dish)
